// Declarando uma variável composta.

let num = [1, 2, 3, 4, 5]

console.log(num)

// Adicionando um valor a array
num[5] = 9

console.log(num)

// Adicionando um valor ao final do array:
num.push(3)

console.log(num)

// Ordenando os valores em ordem cresente dentro da variável
num.sort()

console.log(num)

// Verificando o tamanho da array ou vetor:
console.log(`O tamanho do vetor é de ${num.length} indexizacões.`)