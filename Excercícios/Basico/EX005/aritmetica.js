

// Declarando variável:
let n1 = 15
let n2 = 3

// Calculos:
let soma = n1 + n2
let divi = n1 / n2
let subt = n1 - n2
let mult = n1 * n2
let rest = n1 % n2
let pote = n1 ** n2

// Mostra no console para o usuário.
console.log(`A soma entre ${n1} + ${n2} = ${soma}`)
console.log(`A subtração entre ${n1} - ${n2} = ${subt}`)
console.log(`A multiplicação entre ${n1} * ${n2} = ${mult}`)
console.log(`A divisão entre ${n1} / ${n2} = ${divi}`)
console.log(`O resto da divisão entre ${n1} % ${n2} = ${rest}`)
console.log(`A potenciação entre ${n1} ** ${n2} = ${pote}`)
